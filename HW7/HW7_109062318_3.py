# P(|v-0.75|<0.06) = P(0.69<v<0.81) = P(v=0.7) + P(v=0.8)
# P(x) = C(N,x) * (p^x) * (1-p)^(N-x)
print(math.comb(10,7) * (0.75)**7 * (0.25)**3 + math.comb(10,8) * (0.75)**8 * (0.25)**2)

